def get_ac_prompt(scores, personalization):
    ac_prompt = [
    {"role": "user", "content":
    ac_instruction_dict[scores[1]]
    },
    {"role": "user", "content": 
    "我會給你各個詳細指標的分數及這同學的一些實際行爲，請你綜合這些資料後提供：\n\
    第一段：" + scores[1] + "的學生評論（可以多引用實際行爲）\n\
    第二段：給同學的建議。\n\
    *評語不要直接提到【詳細指標】和【分數】的字眼\n\
    \n\
    " + scores[0] + f' \n\
    詳細指標1：{scores[2]}分 \n\
    詳細指標2：{scores[3]}分 \n\
    詳細指標3：{scores[4]}分 \n\
    \n\
    實際行爲：\n\
    {personalization}'
    }
    ]
    return ac_prompt



def gpt_prompt(section, scores, selected_comments, personalized_comments):
    personalization = ""
    prompt = ""
    print(scores)
    if len(selected_comments) == 0:
        personalization = personalized_comments
    else:
        personalization = selected_comments + [personalized_comments]

    if section == "social_style":
        prompt = get_social_style_prompt(scores, personalization)
    elif section == "s_and_c":
        prompt = get_s_and_c_prompt(scores, personalization)
    elif section == "ac":
        prompt = get_ac_prompt(scores, personalization)
    elif section == "general":
        prompt = get_general_prompt(personalization)

    chat_completion = client.chat.completions.create(
        messages= prompt,
        model= MODEL,
        temperature= 1.1,
        n=1
    )
    return chat_completion.choices[0].message.content
