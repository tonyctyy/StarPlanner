    for i in range(len(s_and_c_instruction_dict[scores[1]][1])):
        score += f"{s_and_c_instruction_dict[scores[1]][1][i]}: {scores[i+2]}\n"
    
    s_and_c_prompt = [
    {"role": "assistant", "content":
    "你是一名學業培導師，接下來將會就一位同學的學生能力模型進行分析並提供評語和注意事項。\
    學生能力模型整合了學生在學習和與人相處時使用8種不同能力的習慣和傾向，評分標準為(1-6分，1分代表學生從不使用該能力，6分代表總是使用該能力)，分數的高低不代表能力的優劣。\
    每項能力由5-6個能力指標組合而成，能力指標提供了更完整和詳細的評分角度，可見行爲說明了能力指標在不同情況下可能出現的方式，典型行爲及其評分則提供了不同分數可參考的評語。\
    在提供評語時不要强調分數，而是提出分數可能代表的表現和行爲。提供評語時亦不用説明就那個能力指標而言，而是把能力指標融入句子中表達。請把同學的實際行爲融入評語和注意事項的描述中。"
    },
    {"role": "assistant", "content":
    f"以下是{s_and_c_instruction_dict[scores[1]][0]}評分標準的細項：\n{s_and_c_instruction_dict[scores[1]][2]}"
    },
    {"role": "user", "content":
    f"請為{scores[0]}進行分析並提供評語和注意事項：1. 評語(請整合成一段):\n 2. 注意事項(請整合成一段):\n\
    以下是{scores[0]}不同能力指標的得分和他的實際行爲，請嘗試在描述中融入他的實際行爲：\n{score}實際行爲：\n{personalization}"}
    ]

    return s_and_c_prompt


